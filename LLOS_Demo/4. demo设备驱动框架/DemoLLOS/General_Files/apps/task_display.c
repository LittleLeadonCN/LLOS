ll_taskId_t taskDisplayId = LL_ERR_INVALID;

ll_taskEvent_t Task0_Events(ll_taskId_t taskId, ll_taskEvent_t events)
{
	if(events & LL_EVENT_MSG)
	{
		LOG_I("task%d receive message: %s\r\n", taskId, LLOS_Msg_Receive());
		LLOS_Msg_Clear();
		return LL_EVENT_MSG;
	}
	if(events & TASK_DISPLAY_EVENT1)
	{
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic0);
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic1);
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic2);
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic3);
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic4);
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic5);
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic6);
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic7);
		LLOS_SSD1306_DrawPic(0, 0, 128, 8, pic8);
		LLOS_Start_Event(taskDisplayId, TASK_DISPLAY_EVENT1, LLOS_Ms_To_Tick(0));
		return TASK_DISPLAY_EVENT1;
	}

	return 0xFFFF;
}

void TaskDisplay_Init(void)
{
    taskDisplayId = LLOS_Register_Events(Task0_Events);
    if(taskDisplayId == LL_ERR_INVALID)
    {
    	LOG_I("TaskDisplay init failed!\r\n");
		while(1);
    }
	
	/* 屏幕初始化 */
	LLOS_SSD1306_HAL_Init();
	LLOS_SSD1306_Fill(0xAA);
	
	LLOS_DelayMs(1000);
	LLOS_SSD1306_Fill(0x00);
	LLOS_SSD1306_ShowString(0, 0, "您好，小叶子。数学℃\r\nHello LLOS");
	
	LLOS_DelayMs(1000);
	LLOS_SSD1306_Fill(0x00);
	LLOS_SSD1306_ShowNumFormat(0, 0, 32767, "%08X", ll_SSD1306_sizeFont_8x16);
	LLOS_SSD1306_ShowNumFormat(0, 2, 32767, "%08X", ll_SSD1306_sizeFont_6x8);
	LLOS_SSD1306_ShowNumFormat(0, 4, -32767, "%d", ll_SSD1306_sizeFont_8x16);
	
	LLOS_DelayMs(1000);
	LLOS_SSD1306_Fill(0x00);
	LLOS_SSD1306_DrawRectangle(0, 0, 16, 16, ll_disable);
	LLOS_SSD1306_DrawRectangle(30, 0, 16, 16, ll_enable);
	LLOS_SSD1306_DrawRoundedRectangle(60, 0, 30, 16, 5, ll_disable);
	LLOS_SSD1306_DrawRoundedRectangle(96, 0, 30, 16, 5, ll_enable);
	LLOS_SSD1306_DrawCircle(8, 30, 8, ll_disable);
	LLOS_SSD1306_DrawCircle(38, 30, 8, ll_enable);
	LLOS_SSD1306_DrawTriangle(5, 60, 15, 60, 10, 50, ll_disable);
	LLOS_SSD1306_DrawTriangle(35, 60, 45, 60, 40, 50, ll_enable);
	
	LLOS_DelayMs(1000);
	LLOS_SSD1306_Fill(0x00);
	
	LLOS_Start_Event(taskDisplayId, TASK_DISPLAY_EVENT1, LLOS_Ms_To_Tick(0));
}

